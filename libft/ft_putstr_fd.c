#include "libft.h"

void	ft_putstr_fd(char *src, int fd)
{
	int	i;

	i = 0;
	if (!src || fd < 0)
		return ;
	while (src[i])
	{
		ft_putchar_fd(src[i], fd);
		i++;
	}
}
